<?php
namespace App\Helpers;
 
use Illuminate\Support\Facades\DB;
 
class Tanggal {
    public static function indo_full($tanggal) {
        $bulan = array (
    1 =>   'Januari',
    'Februari',
    'Maret',
    'April',
    'Mei',
    'Juni',
    'Juli',
    'Agustus',
    'September',
    'Oktober',
    'November',
    'Desember'
  );
  $pecahkan = explode('-', $tanggal);
 
  return $pecahkan[2] . ' ' . $bulan[ (int)$pecahkan[1] ] . ' ' . $pecahkan[0];

    }

      public static function tanggal_bulan($tanggal) {
